///////////////////////////////////////////////////////////////////
//////////  INICIALIZACION PARA ENVIAR DATO O COMANDO  ////////////

void Dato_(void){
	Inicio_Trama();
	I2C_SOFT_Enviar(0x78); // se envia direccion + escritura (0x78)
	I2C_SOFT_Enviar(0x40); // se envia 0x40 para datos
}

void Comando_(void){
	Inicio_Trama();
	I2C_SOFT_Enviar(0x78); // se envia direccion + escritura (0x78)
	I2C_SOFT_Enviar(0x00); // se envia 0x00 para comandos
}

void OLED_init(void){
	//const uint8_t* aux = Config_OLED;

	for(uint8_t i=0; i<27; i++){ // se envian 27 registros de configuracion
		Comando_(); // inicio de trama para enviar comandos al OLED
		I2C_SOFT_Enviar(Config_OLED[i]); 
		Fin_Trama(); 
		//++aux;
	}
}

void OLED_clear(void){
	Dato_(); // inicio de trama para envior datos
	for (uint16_t i=0; i<1024; i++){ I2C_SOFT_Enviar(0); }// envia 8 byte 128 veces para cubrir 128x64 pixeles
	Fin_Trama();
}

void Print_OLED(const char* DATA){
	while(*DATA!=0){ // ciclo infinito si el dato es diferente de 0
		for (uint8_t i=0; i<6; i++){ 
			Dato_(); // inicio de trama para envior datos al OLED
			I2C_SOFT_Enviar(OLED_FONT[(*DATA-32)*6 + i]);
			Fin_Trama();
		}
		DATA++; // incrementa a la siguiente posicion del string
	}
}

void OLED_cursor(uint8_t xpos, uint8_t ypos){
	Comando_(); // inicio de trama para enviar comandos al OLED
	I2C_SOFT_Enviar(xpos & 0x0F); //  nibble bajo para posicionarse en la columna
	I2C_SOFT_Enviar(0x10 | (xpos >> 4)); // nibble alto para posicionarse en la columna
	I2C_SOFT_Enviar(0xB0 | ((ypos>>3) & 0x07)); // byte para posicionarse en la pagina (0 a 7)
	Fin_Trama();
}

void OLED_Print_Pixel(uint8_t Pos_x, uint8_t Pos_y){
    OLED_cursor(Pos_x, Pos_y); 
    Dato_();
    I2C_SOFT_Enviar( (1<<( Pos_y%8)) );
    Fin_Trama();
}